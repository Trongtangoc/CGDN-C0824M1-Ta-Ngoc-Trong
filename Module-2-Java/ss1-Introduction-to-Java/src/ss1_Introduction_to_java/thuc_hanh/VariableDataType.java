package ss1_Introduction_to_java.thuc_hanh;

public class VariableDataType {
    public static void main(String[] args) {
        int i = 10; // khai bao bien i la kieu int co gia tri la 10
        float f = 3.14f; // khai bao bien f la kieu float co gia tri la  3.14
        double d = 3.14; // khai bao bien d la kieu double co gia tri la 3.14
        boolean b = true; // khai bao bien b la kieu boolean co gia tri la true
        char c = 'c';// khai bao bien c la kieu char co gia tri la ki tu c
        String str = "hello"; // khai bao bien str la kieu String co gia tri la chuoi hello

        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("str = " + str);

    }
}
